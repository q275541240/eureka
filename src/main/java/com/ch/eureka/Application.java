import org.springframework.cloud.netflix.eureka.server.EnableEurekaServer;

/**
 * Create By Hong Cui
 * 2018-4-20
 */
@SpringBootApplication
@EnableEurekaServer
public class Application {
    public static void main(String[] args) {
        SpringApplication.run(Application.class, args);
    }
}

